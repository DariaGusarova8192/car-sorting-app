package input.strategy;

import collection.CarList;

/**
 * Общий интерфейс для всех способов заполнения коллекции автомобилей.
 *
 * Это реализация паттерна Strategy для ввода данных.
 *
 * Конкретные стратегии:
 * - ManualCarInputStrategy — ручной ввод;
 * - RandomCarInputStrategy — случайная генерация;
 * - FileCarInputStrategy — чтение из файла.
 *
 * Благодаря этому интерфейсу основная программа может работать
 * с любым способом ввода одинаково.
 */
public interface CarInputStrategy {

    /**
     * Заполняет и возвращает коллекцию автомобилей.
     *
     * В проекте используется кастомная коллекция CarList,
     * а не стандартный ArrayList.
     */
    CarList inputCars();

    /**
     * Возвращает название стратегии ввода.
     *
     * Метод нужен для вывода информации пользователю
     * или для отладки.
     */
    String getStrategyName();
}